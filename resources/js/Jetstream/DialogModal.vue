<script setup>
import Modal from './Modal.vue';

const emit = defineEmits(['close']);

defineProps({
  show: {
    type: Boolean,
    default: false,
  },
  maxWidth: {
    type: String,
    default: '2xl',
  },
  closeable: {
    type: Boolean,
    default: true,
  },
  justify: {
    type: String,
    default: "center"
  },
  items: {
    type: String,
    default: "start"
  }
});

const close = () => {
  emit('close');
};
</script>

<template>
  <Modal
    :closeable="closeable"
    :max-width="maxWidth"
    :show="show"
    @close="close"
    :justify="justify"
    :items="items"
  >
    <div class="px-6 py-4">
      <div class="text-lg">
        <slot name="title"/>
      </div>

      <div class="mt-4">
        <slot name="content"/>
      </div>
    </div>

    <div class="flex flex-row justify-end px-6 py-4 bg-gray-100 text-right rounded-bl-lg rounded-br-lg" v-if="$slots.footer">
      <slot name="footer"/>
    </div>
  </Modal>
</template>
